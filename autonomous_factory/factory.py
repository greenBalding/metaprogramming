def apply_task_handler(
    project_root: Path,
    state: dict[str, Any],
    phase_name: str,
    task_title: str,
) -> dict[str, Any]:
    spec = state.get("spec_snapshot", {})
    file_changes: dict[str, str] = {}
    backup_contents: dict[str, str] = {}

    def write_and_track(relative_path: str, content: str) -> None:
        path = project_root / relative_path
        previous_content = path.read_text(encoding="utf-8") if path.exists() else None
        change_result = write_file_if_changed(path, content)
        if change_result in {"created", "updated"}:
            file_changes[relative_path] = change_result
        if change_result == "updated" and previous_content is not None:
            backup_contents[relative_path] = previous_content

    if task_title == "Validate goal against domain ontology":
        payload = {
            "goal": state.get("goal"),
            "domain": state.get("domain"),
            "result": "compatible",
            "validated_at": datetime.now(timezone.utc).isoformat(),
        }
        write_and_track(
            "execution/validation/domain-check.json",
            json.dumps(payload, indent=2, ensure_ascii=True),
        )
    elif task_title == "Resolve missing constraints and assumptions":
        write_and_track(
            "planning/constraint-resolution.md",
            render_constraint_resolution(spec),
        )
    elif task_title == "Freeze machine-readable specification":
        write_and_track(
            "spec/requirements.lock.json",
            json.dumps(spec, indent=2, ensure_ascii=True),
        )
    elif task_title.startswith("Adopt architecture style:"):
        architecture = state.get("selected_architecture", {})
        notes = [
            "# Architecture Implementation Notes",
            "",
            f"- Selected style: {architecture.get('style', 'unknown')}",
            f"- Backend: {architecture.get('backend', 'unknown')}",
            f"- Database: {architecture.get('database', 'unknown')}",
            f"- Deployment: {architecture.get('deployment', 'unknown')}",
        ]
        write_and_track("architecture/implementation-notes.md", "\n".join(notes))
    elif task_title == "Define API contract and database schema":
        write_and_track("planning/api-contract.md", render_api_contract(spec))
    elif task_title == "Set CI quality gates":
        write_and_track(".github/workflows/ci-generated.yml", render_generated_ci_workflow())
    elif task_title.startswith("Implement module: "):
        module_name = task_title.removeprefix("Implement module: ").strip()
        module_slug = slugify(module_name).replace("-", "_")
        write_and_track(
            f"scaffold/backend/app/modules/{module_slug}.py",
            render_module_stub(module_slug),
        )
    elif task_title == "Run integration scenarios":
        write_and_track(
            "execution/reports/integration-checklist.md",
            "# Integration Scenarios\n\n- [ ] End-to-end enrollment flow\n- [ ] Grade publishing flow\n- [ ] Attendance alert flow\n",
        )
    elif task_title == "Validate security controls":
        write_and_track(
            "execution/reports/security-checklist.md",
            "# Security Controls\n\n- [ ] RBAC validation\n- [ ] Audit log integrity\n- [ ] Input validation checks\n",
        )
    elif task_title == "Execute rollback drill in staging":
        write_and_track(
            "execution/reports/rollback-drill.md",
            "# Rollback Drill\n\n- [ ] Simulate failed deployment\n- [ ] Restore previous artifact\n- [ ] Validate post-rollback health\n",
        )

    return {
        "file_changes": file_changes,
        "backup_contents": backup_contents,
    }


def remove_file_and_empty_parents(path: Path, stop_at: Path) -> bool:
    if not path.exists() or not path.is_file():
        return False
    path.unlink()

    current = path.parent
    while current != stop_at and current.exists():
        try:
            current.rmdir()
        except OSError:
            break
        current = current.parent
    return True


def rollback_last_task(project_root: Path, state: dict[str, Any]) -> dict[str, Any]:
    updated_state = json.loads(json.dumps(state))
    phases = updated_state.get("phase_summary", [])
    rolled_back_event_ids = updated_state.setdefault("rolled_back_event_ids", [])
    now = datetime.now(timezone.utc).isoformat()

    last_task_event = next(
        (
            event
            for event in reversed(updated_state.get("audit_trail", []))
            if event.get("event_type") == "task_execution"
            and event.get("status") == "completed"
            and event.get("id") not in rolled_back_event_ids
        ),
        None,
    )

    if last_task_event is None:
        updated_state["last_action"] = "rollback no-op (no completed task found)"
        updated_state["last_updated"] = now
        append_audit_event(
            updated_state,
            event_type="rollback",
            phase_name="none",
            status="no-op",
            details={"reason": "no completed task in audit trail"},
        )
        return updated_state

    phase_name = str(last_task_event.get("phase", ""))
    task_title = str(last_task_event.get("task", ""))
    details = last_task_event.get("details", {}) or {}
    file_changes = details.get("file_changes", {}) or {}
    backup_contents = details.get("backup_contents", {}) or {}

    reverted_files: list[str] = []
    non_reverted_files: list[str] = []
    removed_evidence = False

    for relative_path, change_type in file_changes.items():
        if change_type == "created":
            removed = remove_file_and_empty_parents(project_root / relative_path, project_root)
            if removed:
                reverted_files.append(relative_path)
            else:
                non_reverted_files.append(relative_path)
            continue

        if change_type == "updated" and relative_path in backup_contents:
            write_file(project_root / relative_path, str(backup_contents[relative_path]))
            reverted_files.append(relative_path)
            continue

        non_reverted_files.append(relative_path)

    for phase_index, phase in enumerate(phases):
        if phase.get("name") != phase_name:
            continue

        for task in phase.get("task_status", []):
            if task.get("title") != task_title:
                continue
            task["status"] = "pending"
            task["last_updated"] = now

            evidence_file = task.get("evidence_file")
            if evidence_file:
                removed_evidence = remove_file_and_empty_parents(
                    project_root / evidence_file,
                    project_root,
                )
            break

        phase["status"] = "in_progress"
        next_index = phase_index + 1
        if next_index < len(phases) and phases[next_index].get("status") == "ready":
            phases[next_index]["status"] = "pending"
        break

    updated_state["history"].append(
        {
            "phase": phase_name,
            "action": "rolled_back_last_task",
            "timestamp": now,
        }
    )
    append_audit_event(
        updated_state,
        event_type="rollback",
        phase_name=phase_name,
        task_title=task_title,
        status="completed",
        details={
            "reverted_files": reverted_files,
            "non_reverted_files": non_reverted_files,
            "removed_evidence": removed_evidence,
            "source_event_id": last_task_event.get("id"),
        },
    )
    if last_task_event.get("id") is not None:
        rolled_back_event_ids.append(last_task_event["id"])
    updated_state["last_action"] = f"rolled back task '{task_title}' in {phase_name}"
    updated_state["last_error"] = None
    updated_state["last_updated"] = now
    return updated_state


def execute_phase_actions(
    project_root: Path,
    state: dict[str, Any],
    dry_run_actions: bool = False,
) -> dict[str, Any]:
    updated_state = json.loads(json.dumps(state))
    phases = updated_state.get("phase_summary", [])
    now = datetime.now(timezone.utc).isoformat()

    active_phase_index = next(
        (index for index, phase in enumerate(phases) if phase.get("status") == "in_progress"),
        None,
    )
    if active_phase_index is None:
        active_phase_index = next(
            (index for index, phase in enumerate(phases) if phase.get("status") == "ready"),
            None,
        )
        if active_phase_index is None:
            updated_state["last_action"] = "no-op"
            updated_state["last_updated"] = now
            append_audit_event(
                updated_state,
                event_type="execution",
                phase_name="none",
                status="no-op",
                details={"reason": "no ready or in_progress phase"},
            )
            return updated_state

        phases[active_phase_index]["status"] = "in_progress"
        updated_state["history"].append(
            {
                "phase": phases[active_phase_index]["name"],
                "action": "started",
                "timestamp": now,
            }
        )
        append_audit_event(
            updated_state,
            event_type="phase_transition",
            phase_name=phases[active_phase_index]["name"],
            status="in_progress",
            details={"mode": "execute_phase"},
        )

    active_phase = phases[active_phase_index]
    executed_count = 0
    skipped_count = 0

    try:
        for task in active_phase.get("task_status", []):
            if task.get("status") == "completed":
                skipped_count += 1
                append_audit_event(
                    updated_state,
                    event_type="task_execution",
                    phase_name=active_phase["name"],
                    task_title=task["title"],
                    status="skipped_already_completed",
                    details={"task_id": task["id"]},
                )
                continue

            evidence_relative_path = Path(task["evidence_file"])
            evidence_path = project_root / evidence_relative_path

            if dry_run_actions:
                append_audit_event(
                    updated_state,
                    event_type="task_execution",
                    phase_name=active_phase["name"],
                    task_title=task["title"],
                    status="planned",
                    details={
                        "task_id": task["id"],
                        "evidence_file": str(evidence_relative_path),
                    },
                )
                continue

            handler_result = apply_task_handler(
                project_root,
                updated_state,
                active_phase["name"],
                task["title"],
            )
            file_changes = handler_result["file_changes"]
            backup_contents = handler_result["backup_contents"]
            changed_files = sorted(file_changes)
            write_file(
                evidence_path,
                render_task_evidence(
                    active_phase["name"],
                    task["title"],
                    task["id"],
                    project_root,
                    changed_files,
                ),
            )
            task["status"] = "completed"
            task["last_updated"] = now
            executed_count += 1
            append_audit_event(
                updated_state,
                event_type="task_execution",
                phase_name=active_phase["name"],
                task_title=task["title"],
                status="completed",
                details={
                    "task_id": task["id"],
                    "evidence_file": str(evidence_relative_path),
                    "changed_files": changed_files,
                    "file_changes": file_changes,
                    "backup_contents": backup_contents,
                },
            )

        all_tasks_completed = all(
            task.get("status") == "completed"
            for task in active_phase.get("task_status", [])
        )
        if all_tasks_completed and not dry_run_actions:
            active_phase["status"] = "completed"
            updated_state["history"].append(
                {
                    "phase": active_phase["name"],
                    "action": "completed",
                    "timestamp": now,
                }
            )
            append_audit_event(
                updated_state,
                event_type="phase_transition",
                phase_name=active_phase["name"],
                status="completed",
                details={"mode": "execute_phase"},
            )

            next_index = active_phase_index + 1
            if next_index < len(phases):
                phases[next_index]["status"] = "ready"
                updated_state["history"].append(
                    {
                        "phase": phases[next_index]["name"],
                        "action": "unblocked",
                        "timestamp": now,
                    }
                )
                append_audit_event(
                    updated_state,
                    event_type="phase_transition",
                    phase_name=phases[next_index]["name"],
                    status="ready",
                    details={"reason": "previous phase completed"},
                )

        if dry_run_actions:
            updated_state["last_action"] = f"planned execution for {active_phase['name']}"
        else:
            updated_state["last_action"] = (
                f"executed {executed_count} tasks in {active_phase['name']}"
            )
        updated_state["last_error"] = None
    except OSError as exc:
        updated_state["last_error"] = str(exc)
        updated_state["last_action"] = f"failed execution in {active_phase['name']}"
        append_audit_event(
            updated_state,
            event_type="execution",
            phase_name=active_phase["name"],
            status="failed",
            details={"error": str(exc)},
        )

    updated_state["last_updated"] = now
    append_audit_event(
        updated_state,
        event_type="execution",
        phase_name=active_phase["name"],
        status="dry-run" if dry_run_actions else "completed",
        details={
            "executed_tasks": executed_count,
            "skipped_tasks": skipped_count,
            "dry_run_actions": dry_run_actions,
        },
    )
    return updated_state


