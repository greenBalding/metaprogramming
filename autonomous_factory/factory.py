def compute_decision_log_hash(decision_log: dict[str, Any]) -> str:
    canonical = json.dumps(
        decision_log,
        ensure_ascii=True,
        separators=(",", ":"),
        sort_keys=True,
    )
    return hashlib.sha256(canonical.encode("utf-8")).hexdigest()


def verify_decision_log_integrity(project_root: Path) -> tuple[bool, str]:
    decision_log_path = project_root / "planning" / "decision-log.json"
    checksum_path = project_root / "planning" / "decision-log.sha256"

    if not decision_log_path.exists():
        return False, "missing planning/decision-log.json"
    if not checksum_path.exists():
        return False, "missing planning/decision-log.sha256"

    decision_log = json.loads(decision_log_path.read_text(encoding="utf-8"))
    embedded_integrity = decision_log.get("integrity", {})
    embedded_hash = embedded_integrity.get("hash")
    if not isinstance(embedded_hash, str) or not embedded_hash:
        return False, "decision-log.json has no embedded integrity hash"

    # Recompute hash without the integrity field to match generation flow.
    normalized_payload = dict(decision_log)
    normalized_payload.pop("integrity", None)
    recalculated_hash = compute_decision_log_hash(normalized_payload)

    checksum_line = checksum_path.read_text(encoding="utf-8").strip()
    checksum_parts = checksum_line.split()
    if len(checksum_parts) < 1:
        return False, "invalid checksum file format"
    checksum_hash = checksum_parts[0]

    if embedded_hash != recalculated_hash:
        return False, "embedded hash mismatch in decision-log.json"
    if checksum_hash != recalculated_hash:
        return False, "checksum mismatch in planning/decision-log.sha256"

    return True, "decision log integrity verified"


def build_execution_state(
    spec: dict[str, Any], architecture: dict[str, Any], backlog: list[dict[str, Any]]
) -> dict[str, Any]:
    phases = []
    for phase_index, phase in enumerate(backlog, start=1):
        phase_modules = [
            task.removeprefix("Implement module: ")
            for task in phase["tasks"]
            if task.startswith("Implement module: ")
        ]
        phases.append(
            {
                "order": phase_index,
                "name": phase["phase"],
                "status": "ready" if phase_index == 1 else "pending",
                "tasks": phase["tasks"],
                "task_status": [
                    {
                        "id": f"P{phase_index}-T{task_index}",
                        "title": task,
                        "status": "pending",
                        "last_updated": None,
                        "evidence_file": str(
                            Path("execution")
                            / "evidence"
                            / f"p{phase_index:02d}"
                            / f"t{task_index:02d}-{slugify(task)}.md"
                        ),
                    }
                    for task_index, task in enumerate(phase["tasks"], start=1)
                ],
                "exit_criteria": phase["exit_criteria"],
                "modules": phase_modules,
            }
        )

    return {
        "goal": spec["goal"],
        "domain": spec["domain"],
        "spec_snapshot": spec,
        "selected_architecture": architecture,
        "phase_summary": phases,
        "history": [],
        "audit_trail": [],
        "rolled_back_event_ids": [],
        "last_action": "initialized",
        "last_error": None,
        "last_updated": datetime.now(timezone.utc).isoformat(),
    }


def append_audit_event(
    state: dict[str, Any],
    event_type: str,
    phase_name: str,
    status: str,
    task_title: str | None = None,
    details: dict[str, Any] | None = None,
) -> None:
    audit_trail = state.setdefault("audit_trail", [])
    audit_trail.append(
        {
            "id": len(audit_trail) + 1,
            "timestamp": datetime.now(timezone.utc).isoformat(),
            "event_type": event_type,
            "phase": phase_name,
            "task": task_title,
            "status": status,
            "details": details or {},
        }
    )


def advance_execution_state(state: dict[str, Any]) -> dict[str, Any]:
    updated_state = json.loads(json.dumps(state))
    phases = updated_state.get("phase_summary", [])
    current_time = datetime.now(timezone.utc).isoformat()

    in_progress_index = next(
        (index for index, phase in enumerate(phases) if phase.get("status") == "in_progress"),
        None,
    )

    if in_progress_index is not None:
        for task in phases[in_progress_index].get("task_status", []):
            if task.get("status") != "completed":
                task["status"] = "completed"
                task["last_updated"] = current_time
        phases[in_progress_index]["status"] = "completed"
        updated_state["history"].append(
            {
                "phase": phases[in_progress_index]["name"],
                "action": "completed",
                "timestamp": current_time,
            }
        )
        append_audit_event(
            updated_state,
            event_type="phase_transition",
            phase_name=phases[in_progress_index]["name"],
            status="completed",
            details={"mode": "advance_phase"},
        )
        next_index = in_progress_index + 1
        if next_index < len(phases):
            phases[next_index]["status"] = "ready"
            updated_state["history"].append(
                {
                    "phase": phases[next_index]["name"],
                    "action": "unblocked",
                    "timestamp": current_time,
                }
            )
            append_audit_event(
                updated_state,
                event_type="phase_transition",
                phase_name=phases[next_index]["name"],
                status="ready",
                details={"reason": "previous phase completed"},
            )
        updated_state["last_action"] = f"completed {phases[in_progress_index]['name']}"
    else:
        ready_index = next(
            (index for index, phase in enumerate(phases) if phase.get("status") == "ready"),
            None,
        )
        if ready_index is None:
            updated_state["last_action"] = "no-op"
        else:
            phases[ready_index]["status"] = "in_progress"
            updated_state["history"].append(
                {
                    "phase": phases[ready_index]["name"],
                    "action": "started",
                    "timestamp": current_time,
                }
            )
            append_audit_event(
                updated_state,
                event_type="phase_transition",
                phase_name=phases[ready_index]["name"],
                status="in_progress",
                details={"mode": "advance_phase"},
            )
            updated_state["last_action"] = f"started {phases[ready_index]['name']}"

    updated_state["last_updated"] = current_time
    return updated_state


def render_task_evidence(
    phase_name: str,
    task_title: str,
    task_id: str,
    project_root: Path,
    changed_files: list[str] | None = None,
) -> str:
    file_lines = "\n".join(f"- {item}" for item in (changed_files or []))
    return (
        f"# Evidence {task_id}\n\n"
        f"- Phase: {phase_name}\n"
        f"- Task: {task_title}\n"
        f"- Result: completed\n"
        f"- Project root: {project_root}\n"
        f"- Changed files:\n{file_lines or '- none'}\n"
    )


def write_file_if_changed(path: Path, content: str) -> str:
    normalized = content.rstrip() + "\n"
    existed_before = path.exists()
    if path.exists() and path.read_text(encoding="utf-8") == normalized:
        return "unchanged"
    write_file(path, normalized)
    if existed_before:
        return "updated"
    return "created"


